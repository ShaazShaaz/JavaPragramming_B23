package OfficeHours;

public class WhileVsDoWhile {
    public static void main(String[] args) {
        // while is repeated if statements for taking correct data from user
        // Do while same with while. but before condition we should make step/staements at least one time

        int n=0;

        do {
            System.out.println("Do=while");

        }while (n<9);

        while (n<9){
            System.out.println("while");
        }
    }
}
