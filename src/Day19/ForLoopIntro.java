package Day19;

public class ForLoopIntro {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("-----------------------");
        // with for loop
        for (int i=1;i<=10;i+=2){  // i:1,3,5,7,9
            System.out.println("Hello World");  // 5 times
        }


    }
}
