package Day05_ArithmeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(25/2);// 12
        System.out.println(25.0/2);
        System.out.println(25d/2);

        int a=100/3; // 33.333
        System.out.println(a);
        double b=100/3; // 33.333  or 33  or 33.0
                          // b = 33 cuz int is divided by int but ans will be in decimal cuz of double
        System.out.println("b = " + b);

        double n1=100.00;
        int n2= 4;  // 100/4= 25 remainder0
        System.out.println(n1%n2);









    }
}
