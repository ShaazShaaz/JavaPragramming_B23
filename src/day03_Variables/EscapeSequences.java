package day03_Variables;

public class EscapeSequences {

    /*: opening multiple comment lines
    \n: new line- enter
    \t: paragraph space- tab
    \": keep in quotes
    \\: normal single backslash



   */

    public static void main(String[] args) {
        System.out.println("Hello everyone");
        System.out.println("How are you today?");
        System.out.println("and I live in Pittsburgh");


        System.out.println("Hello everyone.\nMy name is Shazia \nand I live in Pittsburgh");
        System.out.println("_________________________________");
        System.out.println("\tToday is a great day");

        System.out.println("_________________________________");
        System.out.println("my favourite movie is \"Spiderman\"");

        System.out.println("______________");
        System.out.println("\\");




    }

}
